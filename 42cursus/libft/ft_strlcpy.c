#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	len_str;

	len_str = ft_strlen(src);
	i = 0;
	if (!dst || !src)
		return (0);
	if (!size)
		return (len_str);
	while (i < size - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (len_str);
}
